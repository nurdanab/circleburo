export const metaKeywords = {
  global: {
    ru: keywordCategories.services.ru.slice(0, 10), // Топ-10 ключевых слов
    en: keywordCategories.services.en.slice(0, 10)
  },

  // Функция для получения ключевых слов по странице
  getPageKeywords: (page, language = 'ru', type = 'all') => {
    const pageData = pageKeywords[page];
    if (!pageData || !pageData[language]) return [];

    switch (type) {
      case 'primary':
        return pageData[language].primary || [];
      case 'secondary':
        return pageData[language].secondary || [];
      case 'longtail':
        return pageData[language].longtail || [];
      case 'all':
      default:
        return [
          ...(pageData[language].primary || []),
          ...(pageData[language].secondary || []),
          ...(pageData[language].longtail || [])
        ];
    }
  },

  // Функция для генерации keywords строки для мета-тега
  generateKeywordsString: (page, language = 'ru', limit = 15) => {
    const keywords = metaKeywords.getPageKeywords(page, language, 'all');
    return keywords.slice(0, limit).join(', ');
  }
};

