// Экспорт утилит для работы с ключевыми словами
export const keywordUtils = {
  // Получить все ключевые слова для языка
  getAllKeywords: (language = 'ru') => {
    const allKeywords = [];

    Object.values(keywordCategories).forEach(category => {
      if (category[language]) {
        allKeywords.push(...category[language]);
      }
    });

    return [...new Set(allKeywords)]; // Убираем дубликаты
  },

  // Поиск релевантных ключевых слов
  findRelevantKeywords: (searchTerm, language = 'ru', limit = 5) => {
    const allKeywords = keywordUtils.getAllKeywords(language);
    const relevantKeywords = allKeywords.filter(keyword =>
      keyword.toLowerCase().includes(searchTerm.toLowerCase())
    );

    return relevantKeywords.slice(0, limit);
  },

  // Генерация density карты ключевых слов
  generateKeywordDensity: (text, language = 'ru') => {
    const keywords = keywordUtils.getAllKeywords(language);
    const density = {};

    keywords.forEach(keyword => {
      const regex = new RegExp(keyword, 'gi');
      const matches = text.match(regex);
      if (matches) {
        density[keyword] = matches.length;
      }
    });

    return density;
  }
};

